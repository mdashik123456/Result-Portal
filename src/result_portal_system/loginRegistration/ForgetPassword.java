    private void VerifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VerifyActionPerformed
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/resultportal","root","");

            Statement statement = con.createStatement();
            ResultSet rs = statement.executeQuery("SELECT * FROM User WHERE ID = '"+ID+"' ");

            if(rs.next()){
                
                if(ansTF.getText().equals(rs.getString("SecurityQA"))){
                    newPass.setVisible(true);
                    ConfNewPass.setVisible(true);
                    lable1.setVisible(true);
                    lable2.setVisible(true);
                    ShowPassword.setVisible(true);
                    Change.setVisible(true);
                    wrongMessege.setVisible(false);
                }
                else{
                    wrongMessege.setVisible(true);
                }
                
                statement.close();
                con.close();
                rs.close();
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e, "Error", 0);
        }
    }//GEN-LAST:event_VerifyActionPerformed

    private void ChangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ChangeActionPerformed
        // TODO add your handling code here:
         try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/resultportal","root","");

            Statement statement = con.createStatement();
            
            if(newPass.getText().equals(ConfNewPass.getText())){
                statement.executeUpdate("UPDATE user SET Password = '"+newPass.getText()+"' WHERE ID = '"+ID+"'");
                JOptionPane.showMessageDialog(null, "Password Change Successfully");
                this.setVisible(false);
                
            } else {
                JOptionPane.showMessageDialog(null, "Password not march!", "Error", 0);
            }
            
            statement.close();
            con.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e, "Error", 0);
        }
    }//GEN-LAST:event_ChangeActionPerformed

