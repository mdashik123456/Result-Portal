    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:

        int flagPass = 0;
        int flagReg = 0;
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/resultportal","root","");

            Statement statement = con.createStatement();
            ResultSet result = statement.executeQuery("SELECT * FROM User");

            while(result.next()){
                if (result.getString("ID").equals(checkID.getText()) && result.getString("Phone").equals(checkPhone.getText())){
                    flagReg = 1;
                    if(password.getText().equals(confPass.getText())){
                        statement.executeUpdate("UPDATE user SET Name = '" + name.getText() + "', Email = '"+email.getText()+"', Password = '"+password.getText()+"', SecurityQ = '"+secQ.getSelectedItem().toString()+"', SecurityQA = '"+secQA.getText()+"' WHERE ID = '"+checkID.getText()+"' AND Phone = '"+checkPhone.getText()+"' ");
                        flagPass = 1;
                        JOptionPane.showMessageDialog(null, "Registration Complete", "Registration", 1);
                        this.setVisible(false);
                        break;
                    }
                }

            }
            if(flagReg == 0){
                JOptionPane.showMessageDialog(null, "Sorry! Registration Unsuccessfull", "Registration Error", 0);
            }
            else if(flagPass == 0){
                JOptionPane.showMessageDialog(null, "Password Not Match", "Registration", 0);
            }

            statement.close();
            con.close();
            result.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e, "Error", 0);
        }
    }//GEN-LAST:event_submitActionPerformed

