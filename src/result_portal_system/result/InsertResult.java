    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        readResult();
        c1TF.setEditable(false);
        c2TF.setEditable(false);
        c3TF.setEditable(false);
        c4TF.setEditable(false);
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");  
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/resultportal","root","");
            
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT Course_1, Course_2, Course_3, Course_4 FROM result WHERE ID = '"+getID.getText()+"' AND Semester = '"+selectSemester.getSelectedItem().toString()+"'");
            
            if (rs.next()){
                c1.setText(rs.getString("Course_1"));
                c2.setText(rs.getString("Course_2"));
                c3.setText(rs.getString("Course_3"));
                c4.setText(rs.getString("Course_4"));
                
                if (userType.equalsIgnoreCase("Teacher")){
                    if(c1.getText().equalsIgnoreCase(CourseCode)){
                        c1TF.setEditable(true);
                    } else if(c2.getText().equalsIgnoreCase(CourseCode)){
                        c2TF.setEditable(true);
                    } else if(c3.getText().equalsIgnoreCase(CourseCode)){
                        c3TF.setEditable(true);
                    } else if(c4.getText().equalsIgnoreCase(CourseCode)){
                        c4TF.setEditable(true);
                    }
                } else{
                    c1TF.setEditable(true);
                    c2TF.setEditable(true);
                    c3TF.setEditable(true);
                    c4TF.setEditable(true);
                }
                
            } else{
                JOptionPane.showMessageDialog(null, "ID not found", "error", 0);
            }
            
            rs = st.executeQuery("SELECT Name, Batch, Department FROM student WHERE ID = '"+getID.getText()+"' ");
            if (rs.next()){
                name.setText(rs.getString("Name"));
                batch.setText(rs.getString("Batch"));
                department.setText(rs.getString("Department"));
            }
            
            con.close();
            st.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e, "error", 0);
        }
    }//GEN-LAST:event_searchActionPerformed

