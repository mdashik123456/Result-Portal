    public void readResult(){
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");  
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/resultportal","root","");
            
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM Result WHERE id = '"+getID.getText()+"' AND semester = '"+selectSemester.getSelectedItem()+"' ");
            
            while(rs.next()){
                c1TF.setText(rs.getString("Course_1_Mark"));
                c2TF.setText(rs.getString("Course_2_Mark"));
                c3TF.setText(rs.getString("Course_3_Mark"));
                c4TF.setText(rs.getString("Course_4_Mark"));
                
            }
            
            con.close();
            st.close();
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null, e, "error", 0);
        }
    }
    
