    public final void selectRowFromTable(){
        int row = table1.getSelectedRow();
        String ID0 = table1.getModel().getValueAt(row, 0).toString();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");  
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/resultportal","root","");
            
            Statement statement = con.createStatement();  
            ResultSet rs = statement.executeQuery("SELECT * FROM user WHERE ID = '"+ID0+"' ");  
            
            while(rs.next()){
                id.setText(rs.getString(1));
                name.setText(rs.getString(2));
                phone.setText(rs.getString(3));
                email.setText(rs.getString(4));
                password.setText(rs.getString(5));
                courseCode.setSelectedItem(rs.getString(6));
            }
            
            statement.close();
            con.close();  
            rs.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e, "Error", 0);
        }
    }

    public final void reTable() {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/resultportal", "root", "");

            Statement statement = con.createStatement();
            ResultSet rs = statement.executeQuery("SELECT * FROM user");
            DefaultTableModel tb1 = (DefaultTableModel) table1.getModel();
            tb1.setRowCount(0);

            while (rs.next()) {
                String tableData[] = {rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5), rs.getString(6), rs.getString(7)};
                tb1.addRow(tableData);

            }
            
            id.setText("");
            name.setText("");
            phone.setText("");
            email.setText("");
            password.setText("");
            courseCode.setSelectedItem("None");

            statement.close();
            con.close();
            rs.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e, "Error", 0);
        }
    }

