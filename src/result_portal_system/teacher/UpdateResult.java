    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        String id = getID.getText();
        String course1 = c1TF.getText();
        String semester = selectSemester.getSelectedItem().toString();
        
        if (c1.getText().equalsIgnoreCase("None")){
            JOptionPane.showMessageDialog(null, "Sorry, This course not assigned", "Error", 0);
        } else{
            try{
            Class.forName("com.mysql.cj.jdbc.Driver");  
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/resultportal","root","");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT Name, Batch, Department FROM student WHERE ID = '"+getID.getText()+"'");
            if (rs.next()){
                name.setText(rs.getString("Name"));
                batch.setText(rs.getString("Batch"));
                department.setText(rs.getString("Department"));
            }
            
            st.executeUpdate("UPDATE result SET "+course_name_for_mark+" = '"+c1TF.getText()+"'  WHERE ID = '"+getID.getText()+"' AND Semester = '"+selectSemester.getSelectedItem().toString()+"' ");
            con.close();
            st.close();
            JOptionPane.showMessageDialog(null, "Update Done", "Update", 1);
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null, e, "error", 0);
        }
        }
    }//GEN-LAST:event_updateActionPerformed

    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
//        readResult();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");  
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/resultportal","root","");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT Name, Batch, Department FROM student WHERE ID = '"+getID.getText()+"'");
            if (rs.next()){
                name.setText(rs.getString("Name"));
                batch.setText(rs.getString("Batch"));
                department.setText(rs.getString("Department"));
            }
            
            rs = st.executeQuery("SELECT * FROM result WHERE ID = '"+getID.getText()+"' AND Semester = '"+selectSemester.getSelectedItem().toString()+"' ");
            if(rs.next()){
                if(rs.getString("Course_1").equalsIgnoreCase(CourseCode)){
                    c1.setText(CourseCode);
                    c1TF.setEditable(true);
                    course_name_for_mark = "Course_1_mark";
                    c1TF.setText(rs.getString("Course_1_mark"));
                }else if(rs.getString("Course_2").equalsIgnoreCase(CourseCode)){
                    c1.setText(CourseCode);
                    c1TF.setEditable(true);
                    course_name_for_mark = "Course_2_mark";
                    c1TF.setText(rs.getString("Course_2_mark"));
                }else if(rs.getString("Course_3").equalsIgnoreCase(CourseCode)){
                    c1.setText(CourseCode);
                    c1TF.setEditable(true);
                    course_name_for_mark = "Course_3_mark";
                    c1TF.setText(rs.getString("Course_3_mark"));
                }else if(rs.getString("Course_4").equalsIgnoreCase(CourseCode)){
                    c1.setText(CourseCode);
                    c1TF.setEditable(true);
                    course_name_for_mark = "Course_4_mark";
                    c1TF.setText(rs.getString("Course_4_mark"));
                } else{
                    c1.setText("None");
                    c1TF.setEditable(false);
                }
            }
            con.close();
            st.close();
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null, e, "error", 0);
        }
    }//GEN-LAST:event_searchButtonActionPerformed

